    private function findCollege(string $slug): ?College
    {
        $id = array_search($slug, $this->collegeSlugs, true);

        return College::where('slug', $slug)->first()
            ?? ($id ? College::where('sort_order', $id)->first() : null)
            ?? ($id ? College::find($id) : null);
    }

