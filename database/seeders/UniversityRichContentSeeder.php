                    'description' => $this->programDescriptionHtml($program),
                    'requirements' => $this->programRequirementsHtml($program),
                    'objectives' => $this->programObjectives($program),
                    'careers' => $this->programCareers($program),
                ]);
            }
        });
    }

    private function syncProgramsCount(): void
    {
        College::all()->each(function (College $college) {
            $count = Program::where('college_id', $college->id)->where('is_active', true)->count();
            $college->update(['programs_count' => $count]);
        });

        Department::all()->each(function (Department $department) {
            $count = Program::where('department_id', $department->id)->where('is_active', true)->count();
            if ($count > 0) {
                $department->update(['programs_count' => $count]);
            }
        });
    }

